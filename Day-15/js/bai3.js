// Viết chương trình tính tổng, hiệu, tích, thương, số dư 2 số a = 7, b = 3.
let a = 7;
let b = 3;
let result;
// Tổng hai số là :
result = a + b;
console.log(`Tổng hai số là : ${result}`);

//Hiệu hai số là:
result = a - b;
console.log(`Hiệu hai số là : ${result}`);

//Tích hai số là:
result = a * b;
console.log(`Tích hai số là : ${result}`);

//Thương hai số là:
result = a / b;
console.log(`Thương hai số là : ${result}`);

// Số dư hai số là:
result = a % b;
console.log(`Số dư hai số là : ${result}`);
